function FiledReportview1(id) {

    var base_url = $('#base_url').val();
    var datastring = 'id=' + id;

    $.ajax({
        url: base_url + 'Admin/viewreport1',
        method: 'POST',
        data: datastring,
        success: function(response) {
            //alert(response) 
            $("#modal_body").html(response);
            chnagenewoldreport(2, id);
        }

    });

}

